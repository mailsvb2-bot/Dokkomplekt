"""Offline ICD-10 catalog used by the medical document builder.

The historical public module name is still ``icd10_f_data`` for backward
compatibility, but the catalog is no longer restricted to class V / F00-F99.
It contains:
- all ICD-10 chapter/section rows (A00-Z99 and U00-U99);
- ICD-10 block rows across the full classification;
- the existing detailed F00-F99 rows used by earlier psychiatric workflows;
- a compact set of common cross-specialty rubrics for surgery/therapy/etc.

The chapter rows are localized for every language supported by the program.
Detailed rows keep the trusted Russian wording and are searchable in Russian;
the UI falls back safely where a bundled translation is not present.
"""

from __future__ import annotations

import re

from medical_language_catalog import SUPPORTED_LANGUAGE_IDS, normalize_language_id
from icd10_models import ICD10Diagnosis


_SECTION_KIND = "section"
_BLOCK_KIND = "block"
_RUBRIC_KIND = "diagnosis"
_LANGS = tuple(lang for lang in SUPPORTED_LANGUAGE_IDS if lang != "auto")


def _complete_titles(ru: str, en: str, **localized: str) -> dict[str, str]:
    titles = {"ru": ru, "en": en}
    titles.update({key: value for key, value in localized.items() if value})
    for lang in _LANGS:
        titles.setdefault(lang, titles.get("ru") or titles.get("en") or ru)
    return titles


def _row(code: str, ru: str, en: str = "", *, kind: str = _RUBRIC_KIND, **localized: str) -> ICD10Diagnosis:
    titles = _complete_titles(ru, en or ru, **localized)
    return ICD10Diagnosis(code=code, title=titles["ru"], titles=titles, kind=kind)


