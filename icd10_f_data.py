def _substance_rows() -> list[tuple[str, str]]:
    rows: list[tuple[str, str]] = []
    for code, substance in _SUBSTANCE_TITLES.items():
        rows.append((code, f"Психические и поведенческие расстройства вследствие употребления {substance}"))
        for suffix, state in _SUBSTANCE_STATES.items():
            rows.append((f"{code}.{suffix}", f"Расстройство вследствие употребления {substance}: {state}"))
    return rows


def _build_rows() -> list[ICD10Diagnosis]:
    combined: list[ICD10Diagnosis] = (
        list(_SECTION_ROWS)
        + list(_BLOCK_ROWS)
        + list(_COMMON_RUBRIC_ROWS)
        + [_row(code, title) for code, title in _BASE_ROWS]
    )
    insert_at = len(combined)
    for idx, item in enumerate(combined):
        if item.code == "F20":
            insert_at = idx
            break
    combined[insert_at:insert_at] = [_row(code, title) for code, title in _substance_rows()]

    seen: set[str] = set()
    diagnoses: list[ICD10Diagnosis] = []
    for item in combined:
        if item.code in seen:
            continue
        seen.add(item.code)
        diagnoses.append(item)
    return sorted(diagnoses, key=lambda d: _code_sort_key(d.code))


def _code_sort_key(code: str) -> tuple[int, ...]:
    text = str(code or "").upper()
    letters = re.findall(r"[A-ZА-Я]", text)
    letter_index = ord(letters[0]) - ord("A") + 1 if letters and "A" <= letters[0] <= "Z" else 0
    digits = [int(item) for item in re.findall(r"\d+", text)]
    return (letter_index, *digits)


def _language_complete(item: ICD10Diagnosis) -> bool:
    return all(item.title_for_language(lang) for lang in _LANGS)


def assert_icd10_full_catalog_lock() -> None:
    expected_sections = {
        "A00-B99", "C00-D48", "D50-D89", "E00-E90", "F00-F99", "G00-G99",
        "H00-H59", "H60-H95", "I00-I99", "J00-J99", "K00-K93", "L00-L99",
        "M00-M99", "N00-N99", "O00-O99", "P00-P96", "Q00-Q99", "R00-R99",
        "S00-T98", "V01-Y98", "Z00-Z99", "U00-U99",
    }
    codes = {item.code for item in ICD10_DIAGNOSES}
    missing = sorted(expected_sections - codes)
    if missing:
        raise AssertionError("ICD-10 full chapter catalog is incomplete: " + ", ".join(missing))
    if not any(item.code.startswith("F") for item in ICD10_DIAGNOSES):
        raise AssertionError("ICD-10 catalog lost F-class rows")
    if not any(item.code.startswith("K") for item in ICD10_DIAGNOSES):
        raise AssertionError("ICD-10 catalog is still F-class-only: K-class missing")
    section_rows = [item for item in ICD10_DIAGNOSES if item.kind == _SECTION_KIND]
    for lang in _LANGS:
        if not all(item.title_for_language(lang) for item in section_rows):
            raise AssertionError(f"ICD-10 section title missing for language: {lang}")


ICD10_DIAGNOSES: list[ICD10Diagnosis] = _build_rows()
